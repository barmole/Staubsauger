package de.ole.Staubsauger.Simulation;

public class Schmutz {
    double posX,posY;

    public Schmutz(double posX, double posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public double getPosX() {
        return posX;
    }

    public double getPosY() {
        return posY;
    }
}
