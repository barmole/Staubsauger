package de.ole.Staubsauger;

public class Ladestation {
    double posX,posY;

    public Ladestation(double posX, double posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public double getPosX() {
        return posX;
    }

    public double getPosY() {
        return posY;
    }
}
